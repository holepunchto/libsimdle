#include <assert.h>

#include "../../include/simd.h"

int
main () {
  simd_v128_t v = {
    .u32 = {
      0b00000000000000000000000000000001,
      0b10000000000000000000000000000001,
      0b10000000000000000000000000000011,
      0b11000000000000000000000000000011,
    },
  };

  simd_v128_t r = simd_cnt_v128_u32(v);

  assert(r.u32[0] == 1);
  assert(r.u32[1] == 2);
  assert(r.u32[2] == 3);
  assert(r.u32[3] == 4);
}
