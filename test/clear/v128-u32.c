#include <assert.h>

#include "../../include/simdle.h"

int
main() {
  simdle_v128_t a = {
    .u32 = {
      0b00000000001111111111111111000000,
      0b00000000111111111111111111000000,
    },
  };

  simdle_v128_t b = {
    .u32 = {
      0b00000000000000001111111100000000,
      0b00000000000000111111110000000000,
    },
  };

  simdle_v128_t r = simdle_clear_v128_u32(a, b);

  assert(r.u32[0] == 0b00000000001111110000000011000000);
  assert(r.u32[1] == 0b00000000111111000000001111000000);
}
