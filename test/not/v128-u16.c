#include <assert.h>

#include "../../include/simdle.h"

int
main() {
  simdle_v128_t v = {
    .u16 = {
      0b0000000000000001,
      0b0000000000000011,
      0b0000000000000111,
      0b0000000000001111,
      0b0000000000011111,
      0b0000000000111111,
      0b0000000001111111,
      0b0000000011111111,
    },
  };

  simdle_v128_t r = simdle_not_v128_u16(v);

  assert(r.u16[0] == 0b1111111111111110);
  assert(r.u16[1] == 0b1111111111111100);
  assert(r.u16[2] == 0b1111111111111000);
  assert(r.u16[3] == 0b1111111111110000);
  assert(r.u16[4] == 0b1111111111100000);
  assert(r.u16[5] == 0b1111111111000000);
  assert(r.u16[6] == 0b1111111110000000);
  assert(r.u16[7] == 0b1111111100000000);
}
